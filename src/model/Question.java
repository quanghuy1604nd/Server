package model;

import java.util.UUID;

/**
 *
 * @author QuangHuy
 */
public class Question {
    private UUID id;
    private String code;

    public Question(UUID id, String code) {
        this.id = id;
        this.code = code;
    }

    public UUID getId() {
        return id;
    }

    public void setId(UUID id) {
        this.id = id;
    }

    public String getCode() {
        return code;
    }

    public void setCode(String code) {
        this.code = code;
    }

    
    
}
